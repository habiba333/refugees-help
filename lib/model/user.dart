class User {
  final String imagePath;
  final String name;
  final String email;
  final String phone;
  final String country;
  final String face;
  final String insta;
  final String whats;
  final String about;
  final bool isDarkMode;

  const User({
    required this.imagePath,
    required this.name,
    required this.email,
    required this.phone,
    required this.country,
    required this.face,
    required this.insta,
    required this.whats,
    required this.about,
    required this.isDarkMode,
  });
}
